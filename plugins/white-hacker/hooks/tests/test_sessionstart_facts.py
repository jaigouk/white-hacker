"""Tests for the project-scope SessionStart factual-context hook (T-10.6, TDD).

Run: uv run --project plugins/white-hacker/hooks --with pytest \
       pytest plugins/white-hacker/hooks/tests/test_sessionstart_facts.py -q

The hook reads a SessionStart event on stdin, resolves <cwd>/.white-hacker/project-profile.json
(written by sec-init, T-10.5), and emits the detected profile as FACTUAL additionalContext
(<= 10,000 chars, never imperative). Constraints from spike-07 / ADR-017:
  - additionalContext is capped at 10,000 chars and MUST be factual statements (imperative text
    trips Claude's prompt-injection defenses; white-hacker is itself an injection target).
  - absent/invalid profile -> clean no-op (exit 0, no stdout).
"""
from __future__ import annotations

import io
import json
import subprocess
import sys
from pathlib import Path

import pytest

import sessionstart_project_facts as h

HOOK_PY = Path(h.__file__)

CAP = 10_000


def _full_profile() -> dict:
    return {
        "schema_version": "1.0",
        "generated_note": "Generated by sec-init from repository detection on 2026-06-07.",
        "detected_langs": ["python", "typescript"],
        "frameworks": ["fastapi", "react"],
        "present_capabilities": ["sast", "secrets"],
        "tools_unavailable": ["sca", "iac"],
        "load_appendices": ["lang-python", "lang-typescript"],
        "ai_pass": True,
        "scoring_standard": "CVSS",
        "threat_model_seed": {
            "assets": ["user PII", "session tokens"],
            "entry_points": ["/api/login", "/api/upload"],
            "trust_boundaries": ["public internet -> app"],
        },
    }


def _minimal_profile() -> dict:
    return {
        "schema_version": "1.0",
        "generated_note": "Generated by sec-init.",
        "detected_langs": [],
        "frameworks": [],
        "present_capabilities": [],
        "tools_unavailable": [],
        "load_appendices": [],
        "ai_pass": False,
        "scoring_standard": None,
        "threat_model_seed": {"assets": [], "entry_points": [], "trust_boundaries": []},
    }


def _write_profile(cwd: Path, profile: dict) -> None:
    d = cwd / ".white-hacker"
    d.mkdir(parents=True, exist_ok=True)
    (d / "project-profile.json").write_text(json.dumps(profile))


def _run_main(monkeypatch, event: dict) -> tuple[int, str]:
    """Drive main() with a synthetic stdin event; return (exit_code, stdout)."""
    monkeypatch.setattr(sys, "stdin", io.StringIO(json.dumps(event)))
    out = io.StringIO()
    monkeypatch.setattr(sys, "stdout", out)
    code = h.main()
    return code, out.getvalue()


