def test_main_emits_valid_sessionstart_json(tmp_path, monkeypatch):
    _write_profile(tmp_path, _full_profile())
    code, out = _run_main(monkeypatch, {"hook_event_name": "SessionStart", "cwd": str(tmp_path)})
    assert code == 0
    payload = json.loads(out)
    hso = payload["hookSpecificOutput"]
    assert hso["hookEventName"] == "SessionStart"
    ctx = hso["additionalContext"]
    assert ctx.strip()
    assert h.is_factual(ctx)
    assert len(ctx) <= CAP


def test_main_minimal_profile_emits_valid_small_json(tmp_path, monkeypatch):
    _write_profile(tmp_path, _minimal_profile())
    code, out = _run_main(monkeypatch, {"hook_event_name": "SessionStart", "cwd": str(tmp_path)})
    assert code == 0
    payload = json.loads(out)
    assert payload["hookSpecificOutput"]["hookEventName"] == "SessionStart"
    assert h.is_factual(payload["hookSpecificOutput"]["additionalContext"])


def test_main_no_profile_is_clean_noop(tmp_path, monkeypatch):
    # tmp_path has no .white-hacker/project-profile.json
    code, out = _run_main(monkeypatch, {"hook_event_name": "SessionStart", "cwd": str(tmp_path)})
    assert code == 0
    assert out == "", "absent profile must produce NO stdout"


def test_main_invalid_json_profile_is_clean_noop(tmp_path, monkeypatch):
    d = tmp_path / ".white-hacker"
    d.mkdir(parents=True)
    (d / "project-profile.json").write_text("{ this is not json")
    code, out = _run_main(monkeypatch, {"hook_event_name": "SessionStart", "cwd": str(tmp_path)})
    assert code == 0
    assert out == ""


def test_main_empty_stdin_is_clean_noop(monkeypatch):
    monkeypatch.setattr(sys, "stdin", io.StringIO(""))
    out = io.StringIO()
    monkeypatch.setattr(sys, "stdout", out)
    code = h.main()
    assert code == 0
    assert out.getvalue() == ""


def test_main_falls_back_to_getcwd_when_no_cwd_field(tmp_path, monkeypatch):
    _write_profile(tmp_path, _full_profile())
    monkeypatch.chdir(tmp_path)
    code, out = _run_main(monkeypatch, {"hook_event_name": "SessionStart"})
    assert code == 0
    payload = json.loads(out)
    assert payload["hookSpecificOutput"]["hookEventName"] == "SessionStart"


# ---------------------------------------------------------------------------
# end-to-end subprocess: the real no-op contract (used by the VERIFY demo)
# ---------------------------------------------------------------------------

def test_subprocess_noop_on_missing_cwd(tmp_path):
    event = json.dumps({"hook_event_name": "SessionStart", "cwd": str(tmp_path / "nope")})
    proc = subprocess.run(
        [sys.executable, str(HOOK_PY)],
        input=event,
        capture_output=True,
        text=True,
    )
    assert proc.returncode == 0
    assert proc.stdout == ""
